    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String kodeKerusakan = txtKodeKerusakan.getText();
        String kerusakan = txtKerusakan.getText();
        String solusiKerusakan = txtAreaSolusiKerusakan.getText();
        
        if(kerusakan.equals("") || kodeKerusakan.equals("") || solusiKerusakan.equals("")){
            JOptionPane.showMessageDialog(null, "DATA TIDAK BOLEH KOSONG", "PERINGATAN" , JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                String sql = "INSERT INTO kerusakan VALUES ( id,'" + kodeKerusakan + "', '" + kerusakan + "' , '" + solusiKerusakan + "')";
                st = cn.createStatement();
                st.executeUpdate(sql);
                reset();
                showDataKerusakan();
                setColumnWidth();
                JOptionPane.showMessageDialog(null, "DATA BERHASIL DI SIMPAN");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "DATA GAGAL DI SIMPAN");
                reset();
            }
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

