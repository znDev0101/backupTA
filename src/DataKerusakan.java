    private void showDataKerusakan(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Kode Kerusakan");
        model.addColumn("Kerusakan");
        model.addColumn("Solusi Kerusakan");
        tabelDataKerusakan.setModel(model);
        try {
            st = cn.createStatement();
            String sql = "SELECT * FROM kerusakan";
            rs = st.executeQuery(sql);
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("id") , rs.getString("kode_kerusakan") , rs.getString("nama_kerusakan") , rs.getString("solusi_kerusakan")});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "GAGAL MENAMPILKAN DATA");
        }
        
    }
    
