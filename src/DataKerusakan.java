    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        
        int confirm = JOptionPane.showConfirmDialog(null, "Yakin Data mau di hapus?");
        
        if(confirm == 0){
        try {
            String id = tableDataKerusakan.getValueAt(tableDataKerusakan.getSelectedRow(),0).toString();
            String sql = "DELETE FROM kerusakan WHERE id = '" + id + "'";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data Berhasil di hapus");
            reset();
            showDataKerusakan();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String id = tableDataKerusakan.getValueAt(tableDataKerusakan.getSelectedRow(),0).toString();
        String kodeKerusakan = txtKodeKerusakan.getText();
        String kerusakan = txtKerusakan.getText();
        String solusiKerusakan = txtAreaSolusiKerusakan.getText();
        try {
         st = cn.createStatement();
         String sql = "UPDATE kerusakan SET kode_kerusakan = '" + kodeKerusakan + "',  nama_kerusakan = '" + kerusakan + "', solusi_kerusakan = '" + solusiKerusakan + "' WHERE id = '" + id + "'";
         st.executeUpdate(sql);
         JOptionPane.showMessageDialog(null, "DATA BERHASIL DI UBAH");
         showDataKerusakan();
         reset();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnEditActionPerformed

