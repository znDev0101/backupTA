    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:

        int confirm = JOptionPane.showConfirmDialog(null, "Yakin Data mau di hapus?");

        if(confirm == 0){
            try {
                String id = tblRekapDiagnosa.getValueAt(tblRekapDiagnosa.getSelectedRow(),0).toString();
                String sql = "DELETE FROM rekap_diagnosa WHERE id = '" + id + "'";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil di hapus");
                showRekapDiagnosa();
                setColumnWidth();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tampilDataTabel(String cariData){
        DefaultTableModel model =  (DefaultTableModel) tblRekapDiagnosa.getModel();
        model.setRowCount(0);
        for(Object rows : originalTableModel ){
            Vector rowVector = (Vector) rows;
            for(Object column : rowVector){
                if(column.toString().contains(cariData)){
                    model.addRow(rowVector);
                    break;
                }
            }
        }
    }
    
