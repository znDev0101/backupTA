    private void showRekapDiagnosa(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nama");
        model.addColumn("Nomor Telepon");
        model.addColumn("Gejala Kerusakan");
        model.addColumn("Diagnosa Kerusakan");
        model.addColumn("Solusi Kerusakan");
        tblRekapDiagnosa.setModel(model);
        try {
        st = cn.createStatement();
        String sql = "SELECT * FROM rekap_diagnosa";
        rs = st.executeQuery(sql);
        while(rs.next()){
         model.addRow(new Object[]{rs.getString("id") , rs.getString("nama") , rs.getString("nomor_telepon") , rs.getString("gejala_kerusakan"), rs.getString("diagnosa_kerusakan"),rs.getString("solusi_kerusakan")});
        }
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "GAGAL MENAMPILKAN DATA");
        }
    }
    
